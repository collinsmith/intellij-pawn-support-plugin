package net.alliedmods.lang.amxxpawn.oldpsi2;

/**
 * Represents an AMXX Pawn keyword. Constants defined in this interface represent all keywords of
 * the AMXX Pawn language.
 */
public interface PsiKeyword extends PsiApToken {
  String TRUE = "true";
  String FALSE = "false";

  String ASSERT = "assert";
  String BREAK = "break";
  String CASE = "case";
  String CHAR = "char";
  String CONST = "const";
  String CONTINUE = "continue";
  String DEFAULT = "default";
  String DEFINED = "defined";
  String DO = "do";
  String ELSE = "else";
  String ENUM = "enum";
  String EXIT = "exit";
  String FOR = "for";
  String FORWARD = "forward";
  String GOTO = "goto";
  String IF = "if";
  String NATIVE = "native";
  String NEW = "new";
  String OPERATOR = "operator";
  String PUBLIC = "public";
  String RETURN = "return";
  String SIZEOF = "sizeof";
  String SLEEP = "sleep";
  String STATE = "state";
  String STATIC = "static";
  String STOCK = "stock";
  String SWITCH = "switch";
  String TAGOF = "tagof";
  String WHILE = "while";
}
